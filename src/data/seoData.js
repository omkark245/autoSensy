export function getCanonicalUrl(pathname = '/') {
  const path = pathname === '/' ? '/' : pathname.replace(/\/+$/, '')
  return `${SITE_URL}${path === '/' ? '/' : `${path}/`}`
}

export function getAbsoluteAssetUrl(assetPath = SITE_IMAGE) {
  if (/^https?:\/\//i.test(assetPath)) {
    return assetPath
  }

  return `${SITE_URL}${assetPath.startsWith('/') ? assetPath : `/${assetPath}`}`
}

export function getRouteSeo(pathname = '/') {
  return SEO_ROUTES[pathname] ?? NOT_FOUND_SEO
}

export function getSeoContentPage(pathname = '/') {
  return SEO_CONTENT_PAGES.find((page) => page.path === pathname) ?? null
}
